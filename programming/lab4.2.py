def count_cuts(n):
    # Условие 1: каждому гостю достается хотя бы 1 кусок
    cuts = n

    # Условие 2: как минимум половине гостей достается по 2 куска
    # Находим половину гостей (округление вверх, если n нечетное)
    half_guests = (n + 1) // 2

    # Сначала делим пирог на 2 части
    cuts += 1

    # Теперь обрабатываем половину гостей
    cuts += half_guests - 1  # Количество дополнительных разрезов для удовлетворения условия по кускам

    # Условие 3: каждому гостю достается по 1 куску и остается еще хотя бы 10 кусков
    # Начинаем с делим пирога на 2 части
    cuts += 1

    # Нужно, чтобы первым n гостям досталось n кусков (по одному каждому)
    cuts += n - 1

    # Проверяем, что остается хотя бы 10 кусков
    while cuts < n + 10:
        cuts += 1

    return cuts


def main():
    n = int(input("Введите количество гостей: "))

    if n <= 0:
        print("Введите положительное число гостей.")
        return

    total_cuts = count_cuts(n)
    print(f"Чтобы удовлетворить все условия, понадобится {total_cuts} разрез(ов) пирога.")


if __name__ == "__main__":
    main()